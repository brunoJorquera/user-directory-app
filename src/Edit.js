import React from 'react'
import { data } from './data'

export default function Edit(props) {
    console.log(props)

    return (
        <div>
            {data
            .filter(item => item.id === props.cardNumber)
            .map(item => 

            <div key={item.id} className="content">
                <form 
                onSubmit={(e) => {
                    e.preventDefault()
                    props.handleEditing()
                }}
                    
                className="firstNameInput">
                    <h3>Personal Info</h3>

                    <label> First Name 
                        <input type="text"
                         placeholder={item.name.first}
                         onChange={(e) => {
                            //  item.id - 1 will need us to create autogenerated id's for the new users
                            data[item.id - 1].name.first = e.target.value 
                            }}
                          /></label>

                    <label> Last Name 
                        <input type="text"
                         placeholder={item.name.last}
                          /></label>
        
                        <label> City 
                            <input type='text'
                             placeholder={item.city}
                              /></label>

                        <label> Country 
                            <input type='text'
                             placeholder={item.country}
                              /></label>

                        <label> Job Title 
                            <input type='text'
                             placeholder={item.title}
                              /></label>

                        <label> Employer 
                            <input type='text'
                             placeholder={item.employer}
                              /></label>

                    <br/>

                    <h3>Favorite Movies:</h3>

                    <label> Add Favorites Movies </label>
                    <ol>
                        <input type="text" placeholder={item.favoriteMovies[0]} />
                        <input type="text" placeholder={item.favoriteMovies[1]} />
                        <input type="text" placeholder={item.favoriteMovies[2]} />
                    </ol>

                    <button type="submit" >Save</button>
                </form>
            </div>
            )}
        </div>
    )
}
